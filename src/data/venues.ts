export const getVenueBySlug = (slug: string): Venue | undefined =>
  venues.find((v) => v.slug === slug);

export const getFeaturedVenues = (): Venue[] =>
  venues.filter((v) => v.featured).sort((a, b) => b.mapPriority - a.mapPriority);

export const getVenuesByCity = (city: string): Venue[] =>
  venues.filter((v) => v.city === city);

export const getVenuesByCategory = (category: string): Venue[] =>
  venues.filter((v) => v.category === category);

export const getVenuesByNeighborhood = (neighborhood: string): Venue[] =>
  venues.filter((v) => v.neighborhood === neighborhood);

export const neighborhoods = [...new Set(venues.map((v) => v.neighborhood))].sort();
export const cities = [...new Set(venues.map((v) => v.city))];
export const categories = [...new Set(venues.map((v) => v.category))];
